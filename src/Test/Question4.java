package Test;

public class Question4 {
    public static void main(String[] args) {
        //Create a program that demonstrates pre-increment, post-increment, pre-decrement, and post-decrement operators.

        int i=5;
        System.out.println("Pre-Incremented value is:" + ++i);//6
        System.out.println("Post-Incremented value is:" + i++); //6
        System.out.println("Pre-Decremented value is:" + --i);//6
        System.out.println("Pre-Decremented value is:" + i--);//6

    }
}
